package com.flipkart.constant;

/**
 * This class contains all the constants used throughout the application.
 * It includes color codes for terminal output formatting.
 */
public class Constants {
    // ANSI escape code for Red color
    public static final String RED_COLOR = "\u001B[31m";
    
    // ANSI escape code for Yellow color
    public static final String YELLOW_COLOR = "\u001B[33m";
    
    // ANSI escape code to reset color to default
    public static final String RESET_COLOR = "\u001B[0m";
}
